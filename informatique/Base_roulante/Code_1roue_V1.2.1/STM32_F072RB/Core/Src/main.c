/**
 * @brief Fonction interruption bus CAN
 * quand la STM reçoit un message
 * le message est composé des instructions pour les moteurs
 * @param RxData[0] marche/arrêt (00 ou 01)
 * @param RxData[1] direction (00 ou 01)
 * @param RxData[2] vitesse (FF lent à 05 rapide, au-delà peut poser des problèmes)
 * @param RxData[3] nombre de pas à effectuer
 *
 */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan) {
	/*!< HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData); */
	/*!< Traitement des données */
	uint8_t RxData[8];
	/*!< Réception du message */
	HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData);

	send_one(0x324, 00); /*!< Envoie 00 pour accuser réception */

	repetitions = 1; /*!< Initialisation de repetition */
	motorStop(RxData[0]); /*!< Définie si le moteur est à l'arrêt ou non */
	motorDirection(RxData[1]); /*!< Définie la direction du moteur */
	motorStep(RxData[2]); /*!< Définie la vitesse du moteur */
	target = RxData[3]; /*!< Nombre de pas à effectuer */
	__HAL_TIM_SET_COUNTER(&htim1, 0); /*!< Initialise le compteur du Timer 1 à 0 */

}


